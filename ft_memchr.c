#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char		*target;
	unsigned char			ch;

	target = (const unsigned char *)s;
	ch = (unsigned char)c;
	while (n--)
	{
		if (*target == ch)
			return ((void *)target);
		target++;
	}
	return (NULL);
}
